INSERT INTO dish_tags(dish_id, tag)
SELECT d.id, st.tag
FROM seed_tags st
JOIN dish d ON d.name = st.dish_name
WHERE NOT EXISTS (
    SELECT 1
    FROM dish_tags dt
    WHERE dt.dish_id = d.id AND dt.tag = st.tag
);

